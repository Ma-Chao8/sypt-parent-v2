/**
 * 用户链接信息表
 * <p>
 * Created by zcm on 2019/8/6.
 */
public class UserConnection extends BaseModel {
    private String userId;//` varchar(255) NOT NULL,
    private String providerId;//` varchar(255) NOT NULL,
    private String providerUserId;//` varchar(255) DEFAULT NULL,
    private int rank;//` int(11) NOT NULL,
    private String displayName;//` varchar(255) DEFAULT NULL,
    private String profileUrl;//` varchar(512) DEFAULT NULL,
    private String imageUrl;//` varchar(512) DEFAULT NULL,
    private String accessToken;//` varchar(512) NOT NULL,
    private String secret;//` varchar(512) DEFAULT NULL,
    private String refreshToken;//` varchar(512) DEFAULT NULL,
    private long expireTime;//` bigint(20) DEFAULT NULL

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getProviderId() {
        return providerId;
    }

    public void setProviderId(String providerId) {
        this.providerId = providerId;
    }

    public String getProviderUserId() {
        return providerUserId;
    }

    public void setProviderUserId(String providerUserId) {
        this.providerUserId = providerUserId;
    }

    public int getRank() {
        return rank;
    }

    public void setRank(int rank) {
        this.rank = rank;
    }

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getProfileUrl() {
        return profileUrl;
    }

    public void setProfileUrl(String profileUrl) {
        this.profileUrl = profileUrl;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public void setAccessToken(String accessToken) {
        this.accessToken = accessToken;
    }

    public String getSecret() {
        return secret;
    }

    public void setSecret(String secret) {
        this.secret = secret;
    }

    public String getRefreshToken() {
        return refreshToken;
    }

    public void setRefreshToken(String refreshToken) {
        this.refreshToken = refreshToken;
    }

    public long getExpireTime() {
        return expireTime;
    }

    public void setExpireTime(long expireTime) {
        this.expireTime = expireTime;
    }
}
