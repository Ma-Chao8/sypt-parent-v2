@Service
public class JdbcSocialUserDetailsService implements SocialUserDetailsService {

    @Autowired
    private UserConnectionMapper userConnectionMapper;

    @Override
    public SocialUserDetails loadUserByUserId(String userId) throws UsernameNotFoundException {
        UserConnection userConnection = userConnectionMapper.selectById(userId);
        if (userConnection == null) {
            throw new UsernameNotFoundException("用户不存在");
        }
        return new SocialUser(userId, "", new ArrayList<GrantedAuthority>() {{
            add(() -> {
                return "ANONYMOUS";
            });
        }});
    }
}
